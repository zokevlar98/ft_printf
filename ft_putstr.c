#include "ft_printf.h"

void	ft_putstr(char *s, int *size)
{
	int	i;
	
	if (!s)
	{
		s = "(null)";
		//ft_putstr("(null)", size);
	}
	i = 0;
	while (s[i])
	{
		ft_putchar(s[i], size);
		i++;
	}
}